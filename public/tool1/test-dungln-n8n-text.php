Thông tin công ty HZM Plastic

Công ty Cổ phần nhựa HZM Plastic được thành lập tháng 7 năm 2021 tại Đức Hiệp, Xuân Lâm, Thuận Thành, Bắc Ninh.
Địa chỉ công ty: 10  Đức Hiệp, Xuân Lâm, Thuận Thành, Bắc Ninh, Việt Nam
Tiền thân của HZM Plastic là công ty TNHH Liên Doanh Sản Xuất Khuôn Nhựa Độ Bền Cao HZM (HZM Mold) – được thành lập năm 2017 chuyên sản xuất khuôn ép nhựa đạt chất lượng cao cho các sản phẩm như: Ô tô, xe máy, xe điện, thiết bị phụ trợ công nghiệp, đồ điện, gia dụng,…
Với các kỹ sư chuyên ngành từng làm việc tại Nhật Bản cùng với đội ngũ công nhân có trình độ chuyên môn cao được đào tạo tại các trường nổi tiếng trong nước, HZM có khả năng thiết kế, sản xuất khuôn mẫu, gia công, đúc các sản phẩm nhựa yêu cầu độ chính xác cao.
Với Tổng Diện Tích Hơn 5000m2 cho 2 NHà máy Ép NHựa Với Hơn 20 Máy Từ 120T-1000T & Nhà Máy SX Khuôn Nhựa Độ Bền Cao với nhiều máy CNC, EDM tốc độ cao, máy Lắp rắp khuôn vv.. ! Chúng tôi đặt mục tiêu phát triển thành một công ty sản xuất nhựa và khuôn nhựa tiên tiến hiện đại hàng đầu Việt Nam, trong lĩnh vực sản xuất sản phẩm nhựa, với công nghệ đạt tiêu chuẩn quốc tế.
Bằng chất lượng sản phẩm và giá cả phù hợp cũng như chế độ bảo hành, dịch vụ sau bán hàng chuyên nghiệp, sản phẩm và dịch vụ của chúng tôi ngày càng được nhiều khách hàng lựa chọn.
Sự tin tưởng của quý khách hàng là động lực cho sự phát triển của chúng tôi!


